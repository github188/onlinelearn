package com.bean;

public class ZtreeNode {
       private int id;
       private int pid; //节点的父id;
       private String name; //节点的名称
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
       
       
       
}
